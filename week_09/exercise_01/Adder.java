public class Adder {
    public int Add(int a, int b) {
        return a + b;
    }

    public char Add(char a, char b) {
        return (char) ((int) a + (int) b);
    }

    public double Add(double a, double b) {
        return a + b;
    }

    public String Add(String a, String b) {
        return a + b;
    }
}